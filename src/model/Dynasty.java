package model;

public class Dynasty {
    private String dynastyName;

    public Dynasty() {

    }

    public Dynasty(String dynastyName) {
        this.dynastyName = dynastyName;
    }

    public void setDynastyName(String dynastyName) {
        this.dynastyName = dynastyName;
    }

    public String getDynastyName() {
        return dynastyName;
    }
}
